int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int a;
	int d;
	int i;

	a = 1;
	d = 1;
	i = 1;
	while (i < length)
	{
		if (f(tab[i - 1], tab[i]) > 0)
			d = 0;
		if (f(tab[i - 1], tab[i]) < 0)
			a = 0;
		i++;
	}
	return (a || d);
}
